def plot_bar(data_dict, G, log_values=False):
    x = data_dict.keys()
    y = data_dict.values()

    if log_values:
        y = [log(v) for v in y]

    plt.bar(x, y)
    plt.xlabel("Length of path in grid tiles, G="+str(G))
    if log_values:
        plt.ylabel("Log of number of paths")
    else:
        plt.ylabel("Number of paths")

